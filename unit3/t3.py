# Math function
# Definition : These functions perform mathematical operations on numbers
# Common Functions (in the math module):
# math.sqrt(x): Square root of x.
# math.pow(x, y): x raised to the power of y.
# math.sin(x), math.cos(x): Trigonometric functions.
# math.floor(x), math.ceil(x): Round down/up.
import math

print(math.sqrt(9))  # finds the square root
print(math.pow(2, 3))  # 2^3
print(math.sin(0))  # finds the sin of 0
print(math.cos(0))  # finds the cos of 0
print(math.floor(2.5))  # rounds down => 2
print(math.ceil(2.5))  # rounds up =>3
