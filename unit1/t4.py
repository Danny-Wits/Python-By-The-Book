# String Literal

# String : A string is a series of characters enclosed in single or double quotes
# Generally used to represent text

s1 = "Hello World"  # String Literal
s2 = 'Hello World'  # String Literal
s3 = """Hello
        World"""  # Multiline String Literal
s4 = '''Hello
        World'''  # Multiline String Literal

print(s1, s2, s3, s4, sep="\n")
