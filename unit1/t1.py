# Variables, expressions, and statements

#!Variables : Named containers for storing data
# Syntax : name = value
name = "Bro"
age = 20
print(f"{name} is {age} years old")  # Outputs: Bro is 20 years old
#!Expressions : A combination of values, variables, and operators

x = 5
y = x+3  # x+y is a Expression

#!Statements : A combination of expressions and other statements

x = 5  # x = 5 is a statement
y = x+3  # y = x+3 is a statement
