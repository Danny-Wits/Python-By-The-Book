# Escape Sequences
#! Escape Sequence: Used to insert characters that are illegal in a string
# \n : New Line
# \t : Tab
# \\ : Single Backslash
# \" : Double Quotes
# \' : Single Quotes

print("Hello \nMan")
print("Hello \tMan")
print("Hello \\Man")
print("Hello \"Man\"")
print('Hello \'Man\'')

# Output :
# Hello
# Man
# Hello 	Man
# Hello \Man
# Hello "Man"
# Hello 'Man'
